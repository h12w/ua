func parseList(t *testing.T, list []DupUA) {
	cntErr := 0
	total := 0
	cntDetect := 0
	for _, dupUA := range list {
		dup := dupUA.dup
		ua := dupUA.ua
		total += dup
		device := &Device{LenientParsing: true}
		device, err := device.parse(ua)
		if err != nil {
			//fmt.Println(err)
			cntErr += dup
			continue
		}
		if device.ParsedInfo.Model == "" {
			cntErr += dup
			continue
		}
		if wuDetect(device.ParsedInfo.Model) {
			cntDetect += dup
		} else if _, ok := handsets[device.ParsedInfo.Model]; ok {
			cntDetect += dup
			//fmt.Println(dup, device.Model, ":", ua)
		} else if deviceMapDetect(device.ParsedInfo.Model) {
			cntDetect += dup
		}
		//if wuSearchDetect(device.Model) {
		//	fmt.Println(dup, device.Model, ":", ua)
		//}
	}
	fmt.Println("error count", cntErr, "of", total, float32(cntErr)/float32(total))
	fmt.Println("Detect: ", cntDetect, "of", total, float32(cntDetect)/float32(total))
}

func wuDetect(model string) bool {
	_, ok := deviceInfoMap[model]
	return ok
}

// data from DeviceMap can detect 23% deviceMapModels.
func deviceMapDetect(model string) bool {
	if _, ok := deviceMapModels[model]; ok {
		return true
	} else {
		for m := range deviceMapModels {
			if strings.Contains(model, m) {
				return true
			}
		}
	}
	return false
}

func wuSearchDetect(model string) bool {
	for _, dupUA := range wuList {
		if strings.Contains(dupUA.ua, model) {
			return true
		}
	}
	return false
}

type DupUA struct {
	dup int
	ua  string
}

func loadUAList(file string) (list []DupUA) {
	f, err := os.Open(file)
	if err != nil {
		panic(err)
	}
	defer f.Close()
	s := bufio.NewScanner(f)
	for s.Scan() {
		dupUA := strings.SplitN(s.Text(), " ", 2)
		dup, err := strconv.Atoi(dupUA[0])
		if err != nil {
			panic(err)
		}
		list = append(list, DupUA{dup, dupUA[1]})
	}
	return list
}

